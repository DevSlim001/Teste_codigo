public class Usuario {

	public static String tipoUsu;
	public static Long idUsu;
	public static String nomeUsu;
	public static String getTipoUsu() {
		return tipoUsu;
	}
	public static void setTipoUsu(String tipoUsu) {
		Usuario.tipoUsu = tipoUsu;
	}
	public static Long getIdUsu() {
		return idUsu;
	}
	public static void setIdUsu(Long idUsu) {
		Usuario.idUsu = idUsu;
	}
	public static String getNomeUsu() {
		return nomeUsu;
	}
	public static void setNomeUsu(String nomeUsu) {
		Usuario.nomeUsu = nomeUsu;
	}
	
}
